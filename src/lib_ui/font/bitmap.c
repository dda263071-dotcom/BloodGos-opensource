#include <lib_ui/font/font.h>
#include <libc/string.h>

/* Font 8x16 standar VGA (ASCII 32-126) */
static const uint8_t builtin_font_8x16[256][16] = {

    [0] = {0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00}, /* NULL */
    [32]= {0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00}, /* spasi */
    [33]= {0x00,0x00,0x18,0x3c,0x3c,0x18,0x18,0x00,0x18,0x18,0x00,0x00,0x00,0x00,0x00,0x00}, /* ! */
    [65]= {0x00,0x00,0x18,0x3c,0x66,0x66,0x7e,0x66,0x66,0x66,0x00,0x00,0x00,0x00,0x00,0x00}, /* A */
    /* ... isi lengkap bisa dari file font atau generate nanti */
};

void font_use_builtin(font_t* font) {
    if (!font) return;
    font->width = 8;
    font->height = 16;
    font->bytes_per_glyph = 16;  /* 16 byte per glyph */
    font->is_psf2 = 0;
    font->glyph_data = (uint8_t*)builtin_font_8x16;  /* pointer ke static data */
}

